package Advanced.Medium;

import java.util.ArrayList;
import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

import java.util.ArrayList;
import java.util.Scanner;





public class JavaVisitorPattern {
    enum Color {
        RED, GREEN
    }

    private static abstract class Tree {

        private int value;
        private Color color;
        private int depth;

        public Tree(int value, Color color, int depth) {
            this.value = value;
            this.color = color;
            this.depth = depth;
        }

        public int getValue() {
            return value;
        }

        public Color getColor() {
            return color;
        }

        public int getDepth() {
            return depth;
        }

        public abstract void accept(TreeVis visitor);
    }

    private static class TreeNode extends Tree {

        private ArrayList<Tree> children = new ArrayList<>();

        public TreeNode(int value, Color color, int depth) {
            super(value, color, depth);
        }

        public void accept(TreeVis visitor) {
            visitor.visitNode(this);

            for (Tree child : children) {
                child.accept(visitor);
            }
        }

        public void addChild(Tree child) {
            children.add(child);
        }
    }

    private static class TreeLeaf extends Tree {

        public TreeLeaf(int value, Color color, int depth) {
            super(value, color, depth);
        }

        public void accept(TreeVis visitor) {
            visitor.visitLeaf(this);
        }
    }

    private static abstract class TreeVis
    {
        public abstract int getResult();
        public abstract void visitNode(TreeNode node);
        public abstract void visitLeaf(TreeLeaf leaf);

    }

    private static class SumInLeavesVisitor extends TreeVis {

        int result = 0;

        public int getResult() {
            return result;
        }

        public void visitNode(TreeNode node) {
        }

        public void visitLeaf(TreeLeaf leaf) {
            result += leaf.getValue();
        }
    }

    private static class ProductOfRedNodesVisitor extends TreeVis {

        BigInteger result = BigInteger.ONE;

        public int getResult() {
            return result.mod(BigInteger.valueOf(((long)Math.pow(10,9)) + 7)).intValue();
        }

        public void visitNode(TreeNode node) {
            if(node.getColor() == Color.RED){
                result = result.multiply(BigInteger.valueOf(node.getValue()));
            }
        }

        public void visitLeaf(TreeLeaf leaf) {
            if(leaf.getColor() == Color.RED){
                result = result.multiply(BigInteger.valueOf(leaf.getValue()));
            }
        }
    }

    private static class FancyVisitor extends TreeVis {
        int result = 0;
        public int getResult() {
            return Math.abs(result);
        }

        public void visitNode(TreeNode node) {
            if(node.getDepth() % 2 == 0){
                result += node.getValue();
            }
        }

        public void visitLeaf(TreeLeaf leaf) {
            if(leaf.getColor() == Color.GREEN){
                result -= leaf.getValue();
            }
        }
    }

    private static class Node{
        public Color color;
        public int value;
        public ArrayList<Node> children;
        public Node(int value,Color color){
            this.value = value;
            this.color = color;
            children = new ArrayList<>();
        }
    }

    public static Tree solve() {
        Scanner scan = new Scanner(System.in);
        HashMap<Integer,Node> nodes = new HashMap<>();
        int n = scan.nextInt();
        int[] x = new int[n];
        int[] c = new int[n];
        for(int i = 0; i < n; i++){
            x[i] = scan.nextInt();
        }
        for(int i = 0; i < n; i++){
            c[i] = scan.nextInt();
        }
        for(int i = 0; i < n; i++){
            Node current = new Node(x[i],c[i] == 0 ? Color.RED : Color.GREEN);
            nodes.put(i+1,current);
        }
        for(int i = 0; i < n-1; i++){
            int one = scan.nextInt();
            int two = scan.nextInt();
            nodes.get(one).children.add(nodes.get(two));
            nodes.get(two).children.add(nodes.get(one));
        }
        Node rootNode = nodes.get(1);
        makeTree(null,rootNode);
        TreeNode root = new TreeNode(rootNode.value,rootNode.color,0);
        for(Node child : rootNode.children){
            addChild(root,child);
        }
        return root;
    }

    static void makeTree(Node parent, Node child){
        child.children.remove(parent);
        for(Node c : child.children){
            if(c != parent){
                makeTree(child,c);
            }
        }
    }

    static void addChild(TreeNode parent, Node child){
        if(child.children.size() == 0){
            TreeLeaf leaf = new TreeLeaf(child.value,child.color,parent.getDepth()+1);
            parent.addChild(leaf);
        }
        else{
            TreeNode node = new TreeNode(child.value,child.color,parent.getDepth()+1);
            parent.addChild(node);
            for(Node c : child.children){
                addChild(node,c);
            }
        }
    }

    public static void main(String[] args) {
        Tree root = solve();
        SumInLeavesVisitor vis1 = new SumInLeavesVisitor();
        ProductOfRedNodesVisitor vis2 = new ProductOfRedNodesVisitor();
        FancyVisitor vis3 = new FancyVisitor();

        root.accept(vis1);
        root.accept(vis2);
        root.accept(vis3);

        int res1 = vis1.getResult();
        int res2 = vis2.getResult();
        int res3 = vis3.getResult();

        System.out.println(res1);
        System.out.println(res2);
        System.out.println(res3);
    }
}
